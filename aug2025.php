"수입된개념" 이라는것에 대해 생각하는중.
일단 디아스포라는 수입/수출개념이 훨씬 약할/복잡할수밖에없음.
한국같은나라를 두고본다면, 수입된개념이라는것은 다른나라의영향없이는 그개념이 자생적으로 생기지않았을거라는것을 내포함.
하지만 유교가 중국에서왔다고 수입된개념이라 하는사람은 ㅂㄹ없음.

그러면 수입된개념이라는것은 아직 토착화되지못한개념이라는건가?
좀 붕떠잇는개념?
한국문화가 나름대로 adopt 하고 한국문화에 assimilate 된개념?
한국인이 뼈와살에 느끼지못하는개념?

여기서 사실 "퀴어" 라는개념이 설자리는 논쟁이분분하다.
퀴어는 서구에서만들어진개념이고 (여기서는 국가주의적/탈식민적사유를 핑계로 퀴어혐오를 하는경우도있음) global south, 아시아등으로 수출되었다는주장이있고, 그게아니라 (어떻게보면 더급진적으로, 미국 black studies 등이 이런류의주장을함) cisheteronormativity 자체가 자본주의의재생산을위해 계몽주의때 생겨난개념이며 퀴어는 어떻게보면 그전으로의회귀 + 현대적사유의융합이라는주장.

나는 두번째주장에 더마음이가는데, 사실 첫번째주장을생각하다보면 내퀴어성이 한국인이라는정체성과 맞몰려서 불편해지는건 사실임.
여기서 재밌는점은 퀴혐하는기독교가 수입된개념이냐인데.
나는 일단 서구에서 아무래도 기독교를 더깊이 이해하고있다고생각함.
서구퀴혐기독교는 한국퀴혐기독교랑도결이다른게, 한국퀴혐기독교는 수입된개념인기독교를 찬양하면서 한편으로는 마찬가지로 "수입된개념" 인퀴어는 혐오해야함.

혐오의본질인정상성찬양, xenophobia 에는 수입된개념같은게 설자리는없는데.
그래서 서구퀴혐기독교는 어떻게보면 굉장히 idealist 한반면 한국퀴혐기독은 pragmatic 하다는느낌이듬.
한국에서 실제로 지저스가 신이라고믿는사람이얼마나될까?
교회에 사람만나고 커뮤니티형성하러가지, 신앙심이 (좋은방향으로) 깊으려면 지저스가 왕중의왕이면서 가장낮은곳에서 가장낮은사람을 어루만지며살았다는모순을 두고살아야함.

서구에서는 이모순을 푸는방법이 (혐세는) 지저스가 가장낮은사람을 어루만졌다는건 repress 하고, crucifixion 도 repress 하고, 가족의아버지를 지저스같은존재라고 메타포적으로생각하는것임.
그런데 한국에서는 가부장제가있어도 자기아빠를 두고 지저스의메타포로 생각한다는건 조금웃긴생각임.
가부장제의논리적형식도, 담론도다름.

정리하자면 기독교는 (특히혐세기독교는) 수입된개념의 재미있는케이스인데, 아직기독교가 수입된개념이라고할수있는이유는 혐오의논리적구조가 서구혐세기독교랑다르기때문인듯.
한국유교도 중국유교랑 다르지않냐! 할수있는데 기독교랑 비교하면 생각보다 ㅂㄹ안달른듯...

한국에서 퀴어성과기독교는 어떻게보면 뗄레야뗄수없는사이고 어떻게보면 아무상관없는사이인데, 첫번째케이스는 수입된퀴어, 두번째는 원래있던퀴어라고볼수있지않을까.
퀴어는 항상 theology 를수반하는데 (재생산, 후손, 성에대한것이기때문에 그럴수밖에없음) 두번째는 오히려 한국무당문화에 그계보가있지않을까.

마지막으로한마디: 나는 진짜로디아스포라퀴어성이 "올바른" 퀴어성이며, 퀴어뿐만아니라 모든성에대한고민이다농축되어있기때문에 성에대해생각하는올바른자세라고생각함.
미국물먹고 헛소리하는걸로들릴수있는데, 디아스포라의유대인성을 생각해서하는말임.

최초(는아닐수있지만)의영향력있는디아스포라는 유대인디아스포라였음. (The No State Solution 참조)
여기서 디아스포라는 colonization 과는다른의미이면서도 창의적이며 진취적인뜻을내포함.
남의나라가서살면서 colonization 의학살도하지않고 assimilation 의정체성잃기도하지않고, 새로운문화와교류를이뤄내며 삶을개척하는것.

퀴어성이수입되었는지아닌지에대해 전세계좌파가논쟁하고있는걸보면 퀴어성과유대인성에 재미있는교착점이존재하는것같은데, 유대인성이란디아스포라성이기때문임.

